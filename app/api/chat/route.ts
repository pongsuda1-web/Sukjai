import { NextResponse } from 'next/server';

// Basic Rule-based Chatbot Logic (CBT style)
export async function POST(request: Request) {
  try {
    const { message } = await request.json();
    
    if (!message) {
      return NextResponse.json({ error: 'Message is required' }, { status: 400 });
    }

    // Delay to simulate typing
    await new Promise(resolve => setTimeout(resolve, 1000));

    let reply = "";
    const msg = message.toLowerCase();

    // Simple heuristic keyword matching with randomized responses
    if (msg.includes('เครียด') || msg.includes('เหนื่อย') || msg.includes('ท้อ')) {
      const replies = [
        "ฟังดูเหมือนคุณกำลังแบกรับความรู้สึกหนักๆ อยู่เลยนะคะ 🫂 คุณเก่งมากแล้วที่พยายามผ่านมาจนถึงตอนนี้ อยากเล่าให้น้องน่านสุขใจฟังเพิ่มเติมไหมคะว่าเรื่องอะไรที่ทำให้รู้สึกหนักใจที่สุด?",
        "เหนื่อยมากเลยใช่ไหมคะวันนี้ พักผ่อนบ้างนะคะ ไม่ต้องฝืนเข้มแข็งตลอดเวลาก็ได้ น้องน่านสุขใจอยู่ตรงนี้เสมอค่ะ",
        "บางครั้งความเครียดก็เหมือนก้อนเมฆสีเทานะคะ เดี๋ยวมันก็พัดผ่านไป วันนี้ทำได้ดีที่สุดแล้วค่ะ เล่าให้ฟังต่อได้เสมอนะคะ"
      ];
      reply = replies[Math.floor(Math.random() * replies.length)];
    } else if (msg.includes('เศร้า') || msg.includes('ร้องไห้') || msg.includes('เสียใจ')) {
      const replies = [
        "ความเสียใจเป็นเรื่องธรรมชาตินะคะ อนุญาตให้ตัวเองเศร้าได้เต็มที่เลย การร้องไห้ก็เป็นการระบายอย่างหนึ่งค่ะ ถ้ารู้สึกไม่ไหว กอดตัวเองแน่นๆ นะคะ",
        "น้องน่านสุขใจสัมผัสได้ถึงความเจ็บปวดของคุณนะคะ ร้องไห้ออกมาได้เลยค่ะ พื้นที่ตรงนี้ปลอดภัยสำหรับคุณเสมอ",
        "เวลาที่เราเศร้ามากๆ การมีคนรับฟังอาจช่วยได้นะคะ อยากพิมพ์ระบายอะไรออกมาอีกไหมคะ น้องน่านสุขใจรออ่านอยู่นะ"
      ];
      reply = replies[Math.floor(Math.random() * replies.length)];
    } else if (msg.includes('เหงา') || msg.includes('ไม่มีใคร')) {
      const replies = [
        "ความเหงาเป็นความรู้สึกที่ทำให้เราเคว้งคว้างได้ง่ายๆ แต่คุณไม่ได้อยู่คนเดียวนะคะ น้องน่านสุขใจอยู่ตรงนี้เป็นเพื่อนคุณเสมอ",
        "เวลาที่เราอยู่คนเดียว บางทีความคิดก็ชอบแกล้งเรานะคะ ลองหากิจกรรมเล็กๆ ที่ชอบทำ เช่น ฟังเพลง หรือดูหนัง เพื่อให้ใจกลับมาโฟกัสกับตัวเองดูไหมคะ?",
        "ไม่เป็นไรนะคะที่วันนี้จะรู้สึกเหงา น้องน่านสุขใจจะคอยส่งกำลังใจให้คุณตรงนี้เองค่ะ 😊"
      ];
      reply = replies[Math.floor(Math.random() * replies.length)];
    } else if (msg.includes('แพนิค') || msg.includes('ใจสั่น') || msg.includes('กลัว')) {
      const replies = [
        "ดูเหมือนคุณกำลังกังวลหรือตื่นตระหนกนะคะ หายใจเข้าลึกๆ ช้าๆ 4 วินาที กลั้นไว้ 7 วินาที แล้วค่อยๆ พ่นออก 8 วินาทีนะคะ... มันคืออาการแพนิค เดี๋ยวมันก็ผ่านไปค่ะ คุณปลอดภัยนะคะ 🌿",
        "สูดหายใจเข้าลึกๆ นะคะ ลองมองไปรอบๆ แล้วบอกน้องน่านสุขใจหน่อยว่าตอนนี้คุณเห็นสิ่งของสีฟ้าอะไรบ้าง? (การเบี่ยงเบนความสนใจจะช่วยให้ใจสงบลงค่ะ)",
        "น้องน่านสุขใจเข้าใจเลยว่าความกลัวมันน่าตกใจแค่ไหน แต่คุณทำได้ดีมากที่รู้ตัวนะคะ ลองใช้เมนู 'ฮีลใจด่วน' เพื่อฝึกลมหายใจดูไหมคะ?"
      ];
      reply = replies[Math.floor(Math.random() * replies.length)];
    } else if (msg.includes('โกรธ') || msg.includes('หงุดหงิด') || msg.includes('โมโห')) {
      const replies = [
        "เข้าใจเลยค่ะว่าสถานการณ์นั้นน่าโมโหแค่ไหน ความโกรธบอกให้เรารู้ว่ามีบางอย่างล้ำเส้นเรา ลองเขียนระบายในกล่องระเบิดอารมณ์ของหน้าน่านสุขใจไหมคะ?",
        "เวลาที่เราโกรธ หัวใจจะเต้นแรงมาก ลองถอยออกมาจากสถานการณ์นั้นสัก 5 นาที ดื่มน้ำเย็นๆ แล้วค่อยกลับไปจัดการใหม่นะคะ น้องน่านสุขใจเป็นกำลังใจให้ค่ะ",
        "ระบายความโกรธมาที่น้องน่านสุขใจได้เลยค่ะ! พิมพ์ด่าทอใส่หน้าแชทนี้ก็ได้ เพื่อปลดปล่อยความอึดอัดออกมานะคะ"
      ];
      reply = replies[Math.floor(Math.random() * replies.length)];
    } else if (msg.includes('อยากตาย') || msg.includes('ไม่อยากอยู่')) {
      const replies = [
        "น้องน่านสุขใจอยากบอกว่า ชีวิตของคุณมีค่ามากๆ นะคะ 🥺 หากคุณกำลังรู้สึกไม่ไหวจริงๆ อยากให้ลองโทรปรึกษาสายด่วน 1323 (ฟรี ตลอด 24 ชม.) หรือติดต่อคนใกล้ชิดทันทีนะคะ ขอให้เชื่อว่ายังมีทางออกเสมอ",
        "ได้โปรดให้โอกาสตัวเองอีกนิดนะคะ ความเจ็บปวดที่คุณแบกรับมันหนักหนามาก น้องน่านสุขใจเข้าใจ แต่โลกนี้ยังอยากมีคุณอยู่นะคะ 📞 โทรสายด่วน 1323 เพื่อคุยกับผู้เชี่ยวชาญเถอะนะคะ",
        "ถึงเราจะไม่เคยเจอกัน แต่น้องน่านสุขใจเป็นห่วงคุณจากใจจริงนะคะ อย่าเพิ่งยอมแพ้นะ คุณคู่ควรกับการมีความสุขค่ะ โทรหา 1323 ทันทีเลยนะคะ"
      ];
      reply = replies[Math.floor(Math.random() * replies.length)];
    } else {
      const genericReplies = [
        "ขอบคุณที่แชร์เรื่องราวให้ฟังนะคะ น้องน่านสุขใจรับฟังอยู่นะ อยากเล่าอะไรเพิ่มไหมคะ?",
        "ความรู้สึกของคุณตอนนี้เป็นเรื่องปกตินะคะ ไม่ผิดเลยที่คุณจะรู้สึกแบบนั้น",
        "บางครั้งการได้ระบายออกมาก็ช่วยให้ใจเบาขึ้นได้ค่ะ มีอะไรอยากเล่าต่อนอกเหนือจากนี้ไหมคะ?",
        "คุณเก่งมากแล้วที่กล้าเผชิญหน้ากับความรู้สึกของตัวเอง น้องน่านสุขใจเป็นกำลังใจให้นะคะ 💖",
        "อืมม... น้องน่านสุขใจเข้าใจแล้วค่ะ มีมุมไหนที่คุณรู้สึกกังวลเกี่ยวกับเรื่องนี้เป็นพิเศษไหมคะ?",
        "เล่ามาได้เต็มที่เลยนะคะ พื้นที่ของน่านสุขใจเป็นของคุณ 100% ค่ะ"
      ];
      reply = genericReplies[Math.floor(Math.random() * genericReplies.length)];
    }

    return NextResponse.json({ reply });
  } catch (error) {
    console.error('Error in chat API:', error);
    return NextResponse.json({ error: 'Internal Server Error' }, { status: 500 });
  }
}
